public class Episode {

    private String air_date;

    private String name;

    private String season;

    private String episode;

    public String getAir_date() {
        return air_date;
    }

    public String getEpisodeName() {
        return name;
    }

    public String getSeason() {
        return season;
    }

    public String getEpisode() {
        return episode;
    }
}
